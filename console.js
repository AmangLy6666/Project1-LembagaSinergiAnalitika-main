function simpleArraySum(ar) {
  // Initialize a variable to store the sum
  let sum = 0;

  // Iterate through the array and add each element to the sum
  for (let i = 0; i < ar.length; i++) {
    sum += ar[i];
  }

  // Return the sum
  return sum;
}
